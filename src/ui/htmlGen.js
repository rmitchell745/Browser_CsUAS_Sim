// Extracted from index.html.
// Small HTML helpers stay isolated so rendering code does not duplicate
// escaping rules inline.
function escapeHtml(text) {
      return String(text)
        .replace(/&/g, "&amp;")
        .replace(/</g, "&lt;")
        .replace(/>/g, "&gt;")
        .replace(/"/g, "&quot;")
        .replace(/'/g, "&#39;");
    }
